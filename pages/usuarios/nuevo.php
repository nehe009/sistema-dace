<p>Estimado usuario, para acceder al sistema tómese unos minutos para crear una cuenta. Estos son los pasos a seguir:</p>
<div class="row">
        <div class="col-sm-4">
          <div class="panel panel-success">
            <div class="panel-heading">
              <h3 class="panel-title">Paso #1</h3>
            </div>
            <div class="panel-body">
                <li>Al darle click en “Crear cuenta nueva”, el sistema le presentara un formulario de registro donde deberá escribir su cedula de identidad, a continuación en el siguiente campo su correo electrónico, luego rellenar el captcha el cual es un operación matemática de suma o resta de dos números.</li>
                <li>Darle click en “Registrarse”, el sistema le enviara un correo electrónico a la cuenta indicada.</li>
            </div>
          </div>
        </div>
        <div class="col-sm-4">
          <div class="panel panel-success">
            <div class="panel-heading">
              <h3 class="panel-title">Paso #2</h3>
            </div>
            <div class="panel-body">
                <li>EL sistema le enviara un correo electrónico indicando el nombre de usuario y la contraseña temporal con la que podrá iniciar sesión.</li>
                <li>Antes debe activar el registro, ya sea abriendo el link recibido en el correo o copiando el código de activación recibido en el correo y pegándolo en el formulario que se encuentra en el menú “Activar Cuenta”.</li>
            </div>
          </div>
        </div>
        <div class="col-sm-4">
          <div class="panel panel-success">
            <div class="panel-heading">
              <h3 class="panel-title">Paso #3</h3>
            </div>
            <div class="panel-body">
                <li>Una vez activada la cuenta puede ingresar al sistema usando el menu "Iniciar Sesión", escribiendo en dicho formulario su nombre de usuario, contraseña temporal y resolviendo la operacion matematica.</li>
                <li>El nombre de usuario y la contraseña temporal puede ser cambiada luego en el menú “Mi Perfil”.</li>
            </div>
          </div>
        </div>
      </div>
